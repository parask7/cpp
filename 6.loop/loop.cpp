/* Write a program to print the first 10 natural numbers using both for and while loops. */
#include<iostream>
using namespace std;
int main(){
    // for loop
    for(int i=0;i<=10;i++){
        cout<<i<<"\n";
    }

    // while loop
    int i = 0;
    while(i<=10){
        cout<<i<<"\n";
        i++;
    }
}